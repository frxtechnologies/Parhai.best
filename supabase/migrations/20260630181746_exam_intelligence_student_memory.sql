  using ((select auth.uid())=user_id) with check ((select auth.uid())=user_id);
create policy "Question activity owned access" on public.student_question_activity for all to authenticated
  using ((select auth.uid())=user_id) with check ((select auth.uid())=user_id);
create policy "Mistake history owned access" on public.student_mistake_history for all to authenticated
  using ((select auth.uid())=user_id) with check ((select auth.uid())=user_id);
